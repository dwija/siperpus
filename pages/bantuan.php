				<h2 class="blog-post-title">Tentang SIPERPUS</h2>

				<p>Menjelaskan tentang SIPERPUS</p>
				<hr>
				<p>Sistem Informasi Perpustakaan (SIPERPUS) merupakan sistem informasi yang dikembangkan untuk memudahkan dalam pelayanan serta memudahkan petugas dalam mengelola data anggota, ketersediaan buku, peminjaman buku dan pengembalian buku di perpustakaan.</p>
				<p>Merupakan tugas <i>(Final Project)</i> dari Mata Kuliah <b>Web Programming 1</b> Program Studi Sistem Informasi Universitas Bina Sarana Informatika.</p>
				<p>Sistem Informasi Perpustakaan (SIPERPUS) berbasis web dibuat dengan menggunakan bahasa pemograman web (php, html, css, dan js). Berdasarkan hak aksesnya, SIPERPUS dibagi menjadi dua halaman utama, yaitu halaman untuk <b>publik</b> dan halaman untuk <b>petugas</b> perpustakaan. Adapun Layanan Menunya dapat dijabarkan sebagai berikut:</p>
				<blockquote class="blockquote" id="bantuan1">
				  <p>1. Halaman untuk Publik</p>
				</blockquote>
				<p>Halaman untuk Publik dibuat dengan teknik Pemograman Prosedural. Untuk tampilan dan pengembangan dibantu menggunakan <a href="https://getbootstrap.com/" target="_blank">Bootstrap</a>, adapun menu yang ditampilkan antara lain:</p>
				<ol>
				  <li><a href="profil.html">Profil</a></li>
				  <li><a href="buku.html">Buku</a></li>
				  <li><a href="bantuan.html">Bantuan</a></li>
				  <li><a href="admin">Masuk</a></li>
				  <li><a href="daftar.html">Daftar</a></li>
				</ol>
				<blockquote class="blockquote" id="bantuan2">
				  <p>2. Halaman untuk Petugas</p>
				</blockquote>
				<p>Halaman untuk Petugas dibuat dengan teknik Pemograman Berorientasi Objek dengan konsep MVC <em>(Model View dan Control)</em>. Untuk mempermudah dan agar lebih cepat  dalam pengembangan, kami menggunakan <a href="https://www.codeigniter.com/" target="_blank">CodeIgniter</a> sebagai frameworknya. Untuk dapat mengakses halaman ini diperlukan username dan password sebagai authentikasinya. Adapun fitur yang dibuat antara lain:</p>
				<ul>
				  <li><strong>Dashboard</strong> merupakan halaman Utama petugas perpustakaan</li>
				  <li><strong>Data Pengguna</strong> merupakan halaman untuk manajemen data pengguna (termasuk data member) perpustakaan</li>
				  <li><strong>Data Buku</strong> merupakan halaman untuk manajemen data buku</li>
				  <li><strong>Transaksi</strong> merupakan fitur utama dalam Sistem Informasi Perpustakaan, yaitu untuk menampilkan proses <strong>Peminjaman dan Pengembalian Buku</strong></li>
				</ul>
				
